package com.apiverve.seovalidator.data;

import com.fasterxml.jackson.annotation.*;

public class SEOValidatorData {
    private String url;
    private boolean passed;
    private long entries;
    private String[] output;

    @JsonProperty("url")
    public String getURL() { return url; }
    @JsonProperty("url")
    public void setURL(String value) { this.url = value; }

    @JsonProperty("passed")
    public boolean getPassed() { return passed; }
    @JsonProperty("passed")
    public void setPassed(boolean value) { this.passed = value; }

    @JsonProperty("entries")
    public long getEntries() { return entries; }
    @JsonProperty("entries")
    public void setEntries(long value) { this.entries = value; }

    @JsonProperty("output")
    public String[] getOutput() { return output; }
    @JsonProperty("output")
    public void setOutput(String[] value) { this.output = value; }
}